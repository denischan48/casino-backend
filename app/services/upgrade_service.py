"""
Расчёт шанса успеха апгрейда.

Формула: честный шанс (без запаса казино) — это просто отношение цены того,
что отдаёшь, к цене того, что хочешь получить. Так суммарно на дистанции
выплаты остаются "справедливыми" (математическое ожидание равно тому,
что ставил). House edge немного снижает этот шанс в пользу казино.

Пример: отдаёшь предмет за 1000, целишься в предмет за 4000.
Честный шанс = 1000/4000 = 25%. С house edge 15% -> 25% * 0.85 = 21.25%.
"""

HOUSE_EDGE = 0.15
MIN_CHANCE = 0.01   # никогда не показываем 0% — совсем невозможных апгрейдов не бывает
MAX_CHANCE = 95.0   # и никогда не показываем 100% — совсем без риска апгрейда не бывает


def calc_chance(input_value: int, target_value: int) -> float:
    if target_value <= 0:
        return 0.0
    if input_value >= target_value:
        # апгрейд в предмет ДЕШЕВЛЕ или РАВНОЙ цены — не имеет смысла как "апгрейд",
        # но если фронт всё же прислал такое — даём макс. шанс, ограниченный сверху
        return MAX_CHANCE

    raw_chance = (input_value / target_value) * (1 - HOUSE_EDGE) * 100
    return round(max(MIN_CHANCE, min(MAX_CHANCE, raw_chance)), 2)
